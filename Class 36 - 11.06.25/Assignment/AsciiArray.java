import java.util.*;
class AsciiArray
{
	public static void main(String[] args) 
	{
		char [] a = {'A','B','C','D'};
		for (int i=0;i<a.length ;i++ )
		{
			int b = a[i];
			System.out.println(b);
		}
	}
}
